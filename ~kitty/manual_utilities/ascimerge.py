def ascii_art(fulllarge, fullshort):
    outcome = ""
    short = fullshort.split("\n")
    large = fulllarge.split("\n")
    min = int(len(large) / 2 - len(short) / 2) - 1
    max = int(len(large) / 2 + len(short) / 2)
    i, j = 0, 0
    for lrg in large:
        outcome += lrg + "\t\t"
        if min < i and max > i and j < len(short):
            outcome += short[j]
            j += 1
        i += 1
        outcome += "\n"
    return outcome


